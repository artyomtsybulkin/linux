# Add repository and setup LibreOffice Latest via PPA
sudo add-apt-repository ppa:libreoffice/libreoffice-7-0
sudo apt update
sudo apt install libreoffice libreoffice-style-yaru libreoffice-style-elementary libreoffice-style-sifr

# Install utilities
sudo apt install mc htop curl

# Install Gnome Tweak, extensions and themes
sudo apt install gnome-tweak-tool
sudo apt install gnome-shell-extensions
sudo apt install arc-theme

# Install additional fonts
sudo apt install fonts-noto

# Install Java Runtime
sudo apt install icedtea-netx
